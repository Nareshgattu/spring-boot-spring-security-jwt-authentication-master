package com.bezkoder.springjwt.models;

public class Employee {
	
	private String Name;
	private String age;
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}
	public String getAge() {
		return age;
	}
	public void setAge(String age) {
		this.age = age;
	}
	

}
